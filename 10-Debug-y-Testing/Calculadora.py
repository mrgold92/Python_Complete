
class Calculadora:
    Numero1 = 0
    Numero2 = 0

    def __init__(self) -> None:
        pass

    def Suma(self) -> int:
        return (self.Numero1 + self.Numero2)

    def Resta(self) -> int:
        return (self.Numero1 - self.Numero2)

    def Multiplica(self) -> int:
        return (self.Numero1 * self.Numero2)

    def Divide(self) -> int:
        return (self.Numero1 / self.Numero2)        


